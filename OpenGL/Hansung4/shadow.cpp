void renderTeapot()
{
    // initialize (if necessary)
    if (teapotVAO == 0)
    {
        tinyobj::attrib_t attrib;
        std::vector<tinyobj::shape_t> shapes;
        std::vector<tinyobj::material_t> materials;
        std::string warn, err;

        // teapot.obj 파일 로드 (프로젝트 폴더 기준 경로 조정 필요)
        if (!tinyobj::LoadObj(&attrib, &shapes, &materials, &warn, &err, "../Hansung4/models/teapot.obj")) {
            std::cout << "Failed to load teapot.obj: " << warn << err << std::endl;
            return;
        }

        // 전체 정점 개수 계산
        size_t totalVertices = 0;
        for (const auto& shape : shapes) {
            totalVertices += shape.mesh.indices.size();
        }

        // 배열 할당 (pos 3 + normal 3 + texcoord 2 = 8 floats per vertex)
        size_t bufferSize = totalVertices * 8;
        float* vertices = new float[bufferSize];

        // 데이터 채우기
        size_t p = 0;
        for (const auto& shape : shapes) {
            for (const auto& index : shape.mesh.indices) {
                // Position
                vertices[p++] = attrib.vertices[3 * index.vertex_index + 0];
                vertices[p++] = attrib.vertices[3 * index.vertex_index + 1];
                vertices[p++] = attrib.vertices[3 * index.vertex_index + 2];

                // Normal
                if (index.normal_index >= 0) {
                    vertices[p++] = attrib.normals[3 * index.normal_index + 0];
                    vertices[p++] = attrib.normals[3 * index.normal_index + 1];
                    vertices[p++] = attrib.normals[3 * index.normal_index + 2];
                }
                else {
                    vertices[p++] = 0.0f; vertices[p++] = 1.0f; vertices[p++] = 0.0f; // 기본 노멀
                }

                // TexCoord
                if (index.texcoord_index >= 0) {
                    vertices[p++] = attrib.texcoords[2 * index.texcoord_index + 0];
                    vertices[p++] = attrib.texcoords[2 * index.texcoord_index + 1];
                }
                else {
                    vertices[p++] = 0.0f; vertices[p++] = 0.0f;
                }
            }
        }

        teapotVertexCount = (unsigned int)totalVertices;

        // VAO/VBO 생성 (큐브 코드와 동일한 구조)
        glGenVertexArrays(1, &teapotVAO);
        glGenBuffers(1, &teapotVBO);

        glBindBuffer(GL_ARRAY_BUFFER, teapotVBO);
        glBufferData(GL_ARRAY_BUFFER, bufferSize * sizeof(float), vertices, GL_STATIC_DRAW);

        glBindVertexArray(teapotVBO);
        glEnableVertexAttribArray(0);
        glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)0);
        glEnableVertexAttribArray(1);
        glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(3 * sizeof(float)));
        glEnableVertexAttribArray(2);
        glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, 8 * sizeof(float), (void*)(6 * sizeof(float)));

        glBindBuffer(GL_ARRAY_BUFFER, 0);
        glBindVertexArray(0);

        delete[] vertices; // CPU 메모리 해제
    }

    // 렌더링
    glBindVertexArray(teapotVAO);
    glDrawArrays(GL_TRIANGLES, 0, teapotVertexCount);
    glBindVertexArray(0);
}


// process all input: query GLFW whether relevant keys are pressed/released this frame and react accordingly
// ---------------------------------------------------------------------------------------------------------
void processInput(GLFWwindow* window)
{
    if (glfwGetKey(window, GLFW_KEY_ESCAPE) == GLFW_PRESS)
        glfwSetWindowShouldClose(window, true);

    if (glfwGetKey(window, GLFW_KEY_W) == GLFW_PRESS)
        camera.ProcessKeyboard(FORWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_S) == GLFW_PRESS)
        camera.ProcessKeyboard(BACKWARD, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_A) == GLFW_PRESS)
        camera.ProcessKeyboard(LEFT, deltaTime);
    if (glfwGetKey(window, GLFW_KEY_D) == GLFW_PRESS)
        camera.ProcessKeyboard(RIGHT, deltaTime);
}

// glfw: whenever the window size changed (by OS or user resize) this callback function executes
